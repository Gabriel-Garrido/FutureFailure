const visualTiles: VisualTileData[] = baseTileRects.map((rect, index) => {
  if (rect.width <= 70 || rect.id === 'wall-left-bound' || rect.id === 'wall-right-bound') {
    return tile(rect, elementSprites.terrain.wall.frame);
  }
  if (rect.height >= 80) {
    return tile(rect, index % 2 === 0 ? elementSprites.terrain.floor.frame : elementSprites.terrain.floorPanel.frame);
  }
  const frames = [elementSprites.terrain.ledge.frame, elementSprites.terrain.ledgeHeavy.frame, elementSprites.terrain.ledgeSmall.frame];
  return tile(rect, frames[index % frames.length]);
});

